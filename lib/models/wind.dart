class WindSpeed {
  final double speed;
  final String date;
  final String time;

  WindSpeed({
    required this.speed,
    required this.date,
    required this.time,
  });
}

class WindDirection {
  final double direction;
  final String date;
  final String time;

  WindDirection({
    required this.direction,
    required this.date,
    required this.time,
  });
}
